def create_slug(name_in: str, year: int, sep: str = "-"):
    result = []
    for i in name_in.lower():
        result.append(t.get(i) if i in t else i)

    str_result = "".join(result).replace(" ", sep)

    return f"{str_result}{sep}{year}"
